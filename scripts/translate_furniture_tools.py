def translate_keywords(keywords):
    """翻译关键词数组"""
    translated = []
    for kw in keywords:
        # 尝试完整匹配
        if kw.lower() in KEYWORD_TRANSLATIONS:
            translated.append(KEYWORD_TRANSLATIONS[kw.lower()])
        else:
            # 部分匹配和替换
            translated_kw = kw
            for en, zh in KEYWORD_TRANSLATIONS.items():
                if en in translated_kw.lower():
                    translated_kw = translated_kw.replace(en, zh)
            translated.append(translated_kw)
    return translated

