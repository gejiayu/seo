def process_file(en_file_path, zh_dir):
    """处理单个JSON文件"""
    with open(en_file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # 翻译各个字段
    zh_data = {
        "title": translate_title(data.get("title", "")),
        "description": translate_title(data.get("description", "")),
        "content": translate_content(data.get("content", "")),
        "seo_keywords": translate_keywords(data.get("seo_keywords", [])),
        "slug": data.get("slug", ""),
        "published_at": "2026-05-02",  # 使用当前日期
        "author": "家具租赁行业专家",
        "pros_and_cons": {
            "pros": [translate_content(p) for p in data.get("pros_and_cons", {}).get("pros", [])],
            "cons": [translate_content(c) for c in data.get("pros_and_cons", {}).get("cons", [])]
        },
        "faq": [
            {
                "question": translate_content(faq.get("question", "")),
                "answer": translate_content(faq.get("answer", ""))
            }
            for faq in data.get("faq", [])
        ],
        "language": "zh-CN",
        "canonical_link": f"https://www.housecar.life/zh/posts/{data.get('slug', '')}",
        "alternate_links": {
            "en-US": f"https://www.housecar.life/posts/{data.get('slug', '')}",
            "zh-CN": f"https://www.housecar.life/zh/posts/{data.get('slug', '')}"
        }
    }

    # 保存中文JSON
    zh_file_path = os.path.join(zh_dir, os.path.basename(en_file_path))
    with open(zh_file_path, 'w', encoding='utf-8') as f:
        json.dump(zh_data, f, ensure_ascii=False, indent=2)

    return zh_file_path

