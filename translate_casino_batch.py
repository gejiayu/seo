def translate_title(title):
    # Keep English product names, translate descriptive parts
    translated = title

    if "Top 10" in translated:
        translated = translated.replace("Top 10", "十大")
    if "- 2026 Review" in translated:
        translated = translated.replace("- 2026 Review", "对比2026")
    if "Complete Guide to" in translated:
        translated = translated.replace("Complete Guide to", "完整指南：")
    if "Comprehensive analysis of" in translated:
        translated = translated.replace("Comprehensive analysis of", "全面分析")

    # Apply phrase translations
    for eng, chi in phrase_translations.items():
        if eng in translated and len(eng) > 3:  # Skip short words to avoid over-translation
            translated = translated.replace(eng, chi)

    return translated

