def translate_keywords(keywords):
    translated_keywords = []
    for kw in keywords:
        translated_kw = kw
        for eng, chi in phrase_translations.items():
            if eng in translated_kw and len(eng) > 3:
                translated_kw = translated_kw.replace(eng, chi)
        translated_keywords.append(translated_kw)
    return translated_keywords

