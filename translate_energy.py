def process_file(filepath):
    """Process single JSON file."""
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Translate fields
    if 'title' in data:
        data['title'] = translate_text(data['title'])
    
    if 'description' in data:
        data['description'] = translate_text(data['description'])
    
    if 'content' in data:
        data['content'] = translate_html_content(data['content'])
    
    if 'author' in data:
        data['author'] = translate_author(data['author'])
    
    if 'seo_keywords' in data:
        data['seo_keywords'] = translate_keywords(data['seo_keywords'])
    
    # Write back
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    return filepath.name

