def translate_text(text):
    """Translate Chinese text to English."""
    if not text or not any('一' <= c <= '鿿' for c in text):
        return text
    
    result = text
    
    # Sort by length (longest first) to avoid partial replacements
    sorted_translations = sorted(TRANSLATIONS.items(), key=lambda x: len(x[0]), reverse=True)
    
    for chinese, english in sorted_translations:
        result = result.replace(chinese, english)
    
    return result

def translate_html_content(html):
    """Translate HTML content preserving structure."""
    if not html:
        return html
    
    return translate_text(html)

def translate_keywords(keywords):
    """Translate SEO keywords."""
    if not keywords:
        return keywords
    
    return [translate_text(kw) for kw in keywords]

